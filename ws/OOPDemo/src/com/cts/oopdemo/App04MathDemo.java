package com.cts.oopdemo;

public class App04MathDemo {

	public static void main(String[] args) {
		
		System.out.println(Math.PI);
		
		System.out.println(Math.sin(0));
		System.out.println(Math.sin(Math.PI/2));	//sin of 90 degress
		System.out.println(Math.sin(Math.PI));		//sin of 180 degrees
		
		System.out.println(Math.pow(2, 5));
		System.out.println(Math.exp(5));
		
		System.out.println(Math.log(4));
		System.out.println(Math.log10(4));
		
		System.out.println(Math.floor(13.4));
		System.out.println(Math.floor(13.7));
		System.out.println(Math.round(13.4));
		System.out.println(Math.round(13.7));
		System.out.println(Math.ceil(13.4));
		System.out.println(Math.ceil(13.7));

	}

}
